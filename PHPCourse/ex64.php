<?php
// Esse código demonstra o uso do operador de incremento pré-fixo em PHP.
// O operador ++ antes da variável incrementa o valor da variável antes de usá-la.
// Neste exemplo, a variável $x é incrementada em 1 antes de ser exibida.
// O resultado será 11, pois $x é incrementado de 10 para 11
$x = 10;  
echo ++$x;
