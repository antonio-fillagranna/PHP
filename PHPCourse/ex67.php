// Esse código demonstra o uso do operador de decremento pós-fixado em PHP.
// O operador de decremento pós-fixado (--) diminui o valor da variável após 
// a expressão ser avaliada. Neste exemplo, o valor de $x é 10, e ao usar
// o operador $x--, o valor 10 é retornado antes de $x ser decrementado para 9.
// Portanto, a saída será 10.
