<?php
/*
Criando (Declarando) Variáveis em PHP
No PHP, uma variável começa com o símbolo $, seguido pelo nome da variável.
-> Observação: Ao atribuir um valor de texto a uma variável, coloque aspas ao redor do valor.
-> Dica importante: Diferente de outras linguagens de programação, o PHP não possui um comando 
específico para declarar uma variável. Ela é criada automaticamente no momento em que recebe seu primeiro valor.
Essa abordagem do PHP torna a manipulação de variáveis mais dinâmica e flexível!
*/
$txt = "Olá mundo!";
$x = 5;
$y = 10.5;

echo $txt;
echo "<br>";
echo $x;
echo "<br>";
echo $y;
